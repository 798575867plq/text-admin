export default function textToEmoji(val, size) {
    val = val.replace(/\[(.+?)\]/g, (tag) => {
        Emoticon.forEach(item => {
            if (tag == item.Tag) {
                tag = `<img src="${item.File}" border="0" style="width:${size}px;height:${size}px"/>`
            }
        })
        return tag;
    });
    return val;
}